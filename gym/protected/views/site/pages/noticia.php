<h2>No corras sólo. Comparte tu rendimiento</h2>
<div class="ui segment">
  <img class="ui right floated image" src="images/noticia2.jpg">
  <p>Cada vez son más quienes practican deporte al aire libre. Son los nuevos aventureros, solitarios o en grupo, conocidos como Runners, Trekkers, Cyclers, BTT (corredores, senderistas, o ciclistas-mountainbikers, según la disciplina, por su terminología en inglés).<BR>
  <BR>
 Existen tantas razones para practicar ejercicio, que cada uno encuentra las suyas, aunque comenzar a hacer ejercicio suele ser un suplicio para muchas personas que llevan una vida sedentaria.<BR>
 <BR>
Lo cierto es que nuestro cuerpo está hecho para moverse y ser activo. No es necesario ser más rápidos ni tener la mayor resistencia. Lo importante es manterlo activo y tonificado.<BR>
<BR>
Los expertos recomiendan una actividad física de unos 30 minutos al día para mantener en forma nuestro sistema cardiovascular, mejorar nuestra capacidad pulmonar, prevenir el sobrepeso, mejorar nuestras defensas, y conseguir mejor tono vital, energía y estado anímico. Algunos estudios demuestran que la actividad física nos hace más inteligentes, mejora el flujo de sangre oxigenada a nuestro cerebro.<BR>
<BR>
Si todo son ventajas, ¿Por qué nos cuesta tanto hacer ejercicio regularmente?<BR>
<BR>
Seguramente, la falta de resultados a corto plazo o establecer unos objetivos demasiado ambiciosos, favorecen la pérdida de motivación y el abandono. Al mismo tiempo, nos resulta difícil encontrar compañía (de nuestro nivel o similar) con la que hacer más llevadera la fatiga y los pequeños logros diarios.<BR>
