<h2>Vida fitness a base de piña</h2>
<div class="ui segment">
  <img class="ui left floated image" src="images/noticia3.jpg">
  <p>Los beneficios quemagrasa de esta fruta cada vez son más utilizados en las 
  rutinas de adelgazamiento y dietas alimenticias de los nutricionistas en personas 
  con sobrepeso o que simplemente desean mantenerse en forma. Los efectos que brinda 
  la piña son evidentes al acompañarse con ejercicios físicos.<BR>
   <BR>
Someter tu cuerpo a una exhaustiva rutina de ejercicios diarios tendrá pocos resultados si no los acompañas con nutrientes que las mismas frutas te aportan, como por ejemplo la piña.<BR>
<BR>
Este fruto tropical fibroso es rico en agua. Por esa misma razón, al comerla sacia la sed, y más cuando se está haciendo esfuerzo físico, mediante el cual con el sudor se pierden abundantes líquidos. Según un estudio publicado en el diario americano de la nutrición clínica, la piña, además de estar asociada a un bajo aporte de calorías, que es ideal para adelgazar, es un recurso potente para eliminar grasa por su concentrada enzima bromelina, focalizada especialmente en su jugo y tallo. Asimismo, favorece a la circulación de la sangre, para que no se formen coágulos.<BR>
<BR>
<BR>
<BR>
Al ingerir proteínas la piña ayuda a descomponerlas y es por ello que es muy empleada en tratamientos de obesidad ya que da la sensación de ligereza y es energizante.<BR>
<BR>
Su consumo beneficia al sistema inmunológico y fortalece los huesos pues contiene manganeso, básico para el desarrollo de estos.<BR>
<BR>
Por su contenido en vitamina C o ácido ascórbico es efectiva en la cura de resfriados, gripes y alergias pues reduce la mucosidad en la garganta.<BR>
<BR>
En China es bastante usada para eliminar lombrices intestinales. Otra de sus bondades es que elimina
<BR>
microorganismos y parásitos que causan dolores crónicos y, en casos más serios, diarrea.<BR>
<BR>
Fuente: menshealth.es 13 de mayo de 2015

