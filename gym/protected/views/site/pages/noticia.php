<h2>El monstruo del zumba</h2>
<div class="ui segment">
  <img class="ui left floated image" src="images/noticia1.jpg">
  <p>El colombiano Beto Pérez puso al mundo a 'zumbar' con sus ritmos 
  latinos gracias a una casualidad. Su aeróbic ha llegado ya a 180 países 
  y su empresa está valorada en 400 millones<BR>
  <BR>
  La instrucción es simple: haga que la gente sonría mientras quema cientos
   de calorías durante una hora de ejercicio. ¿La recompensa? Cumplir un sueño 
   y ser millonario. A Alberto Beto Pérez (Cali, 1970) la fórmula lo pilló de sorpresa.
    Un día se dio cuenta de que si ponía a sus alumnos de aeróbica a gozar mientras trabajaban 
    sus cuerpos, con ritmos de merengue, salsa y reggaeton, estos acababan la clase extasiados,
     deseando que empezara la próxima. Eso sí, era 1986 y los millones aún estaban por llegar.
      La génesis del ya famoso zumba se resume en dos palabras: suerte y oportunidad. 
      Por aquel tiempo, su creador era un joven y humilde instructor de gimnasio
       que una mañana se dejó en casa las melodías ochenteras con las que daba clase en su Cali natal.
        “Parece una historia de marketing”, reconoce, convertido en el artífice de una empresa 
        valorada en casi 400 millones de euros y que cuenta con 15 millones de
         aficionados en 180 países. “No tenía la música, pero no podía quedar 
         mal ante mi jefe, así que cogí mis casetes grabados de la radio, 
         con merengue y salsa, y me inventé que había preparado una clase nueva. Improvisé”.
          El resultado fue tan inesperado como victorioso. La gente disfrutaba y 
          los 60 minutos de suplicio se convirtieron en un suspiro. 
          “Así supe lo que quería hacer el resto de mi vida”, cuenta Pérez.
           Había nacido el zumba, aunque él aún no lo sabía.<BR>
<BR>
           Fuente: elpais.com 25 enero 2015</p>
