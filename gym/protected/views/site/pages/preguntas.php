   <h2>¿Hay lockers, y tienen candado?, ¿el Locker es de uso permanente?  </h2>
<div class="ui segment">
    <p>Sí existen Lockers a tu disposición, pero debes  traer tu propio candado.  
Los  Lockers son sólo para que dejes tus cosas mientras te encuentres en el gimnasio, no se 
asignan por persona de manera definitiva, por lo que tienes que traer tu candado siempre 
contigo y sacarlo junto a tus cosas cuando termines de entrenar, de modo que siempre haya 
casilleros disponibles para todos. Si al final del día quedan lockers con candado, estos serán 
abiertos por el personal de Hypertrophia  y las cosas serán retiradas y almacenadas en el armario 
de cosas perdidas. </p>
</div>

