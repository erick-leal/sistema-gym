  <h2> Evalúate</h2>
  <div class="ui segment">
  <img class="ui left floated image" src="images/pic13.jpg">
    <p>Una vez inscrito, un profesor o Personal 
    Trainer se contactará contigo para que vengas a una evaluación.<BR>
    <BR>
    Calcularemos tu porcentaje de grasa, agua y musculatura y tomaremos tus medidas.<BR>
    <BR>
    Según tus objetivos, diseñaremos un programa de entrenamiento personalizado y 
    cada 30 días te reevaluamos para observar tu progreso.<BR>
    <BR>
	También ofrecemos evaluaciones nutricionales los viernes, 
	porque la alimentación es un componente fundamental 
	si quieres lograr los mejores resultados.</p>
  </div>
  <BR>
  <h2>¡Prepárate!</h2> 
  <div class="ui segment">
  <img class="ui right floated image" src="images/pic11.jpg">
    <p>Puedes empezar entrenando el mismo día en que te inscribes o cuando quieras.<BR>
    <BR> 
    Pero es importante que vengas preparado: tenida deportiva, zapatillas, toalla pequeña
     y botella para tomar agua. No olvides tu candado para el locker.<BR>
     <BR> 
     Para la evaluación, es necesario no haber hecho ejercicio antes.</p>
  </div>
